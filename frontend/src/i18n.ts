import { ref } from 'vue'

const savedLang = (typeof localStorage !== 'undefined' ? localStorage.getItem('pubgbar_lang') : null) as Lang | null
const currentLang = ref<Lang>(savedLang || 'zh')

export function t(key: string): string {
  return messages[currentLang.value]?.[key] || messages.zh[key] || key
}

export function getLang(): Lang {
  return currentLang.value
}

export function setLang(lang: Lang) {
  currentLang.value = lang
  if (typeof localStorage !== 'undefined') {
    localStorage.setItem('pubgbar_lang', lang)
  }
}

export function toggleLang() {
  setLang(currentLang.value === 'zh' ? 'en' : 'zh')
}
